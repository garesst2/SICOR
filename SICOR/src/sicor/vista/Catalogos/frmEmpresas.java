    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            EmpresasJpaController ctrEmpresas = new EmpresasJpaController(emf);
            if (validarDatos()) {
                ingreDatosEmpresaConID();
                ctrEmpresas.edit(empresa);
                JOptionPane.showMessageDialog(null, "Se ha modificado con exito");
                actualizarDatos();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "El código de empresa y la razón social deben de ser completados");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error: " + e.toString());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            EmpresasJpaController ctrEmpresas = new EmpresasJpaController(emf);
            ingreDatosEmpresaConID();
            ctrEmpresas.destroy(empresa.getIdEmpresa());
            JOptionPane.showMessageDialog(null, "Se ha eliminado con exito");
            actualizarDatos();
            limpiar();
        } catch (HeadlessException | IllegalOrphanException | NonexistentEntityException e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error: " + e.toString());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

