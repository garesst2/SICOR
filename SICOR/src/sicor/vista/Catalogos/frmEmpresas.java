    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            EmpresasJpaController ctrEmpresas = new EmpresasJpaController(emf);
            if (validarDatos()) {
                ingreDatosEmpresaSinID();
                ctrEmpresas.create(empresa);
                JOptionPane.showMessageDialog(null, "Se ha ingresado con exito");
                actualizarDatos();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "El código de empresa y la razón social deben de ser completados");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error: " + e.toString());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

