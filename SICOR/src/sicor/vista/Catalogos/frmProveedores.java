    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         if (validarNombre()) {
            try {
                ProveedoresJpaController ctlProv = new ProveedoresJpaController(emf);
                recoderDatos();
                proveedor.setIdEmpresa(new EmpresasJpaController(emf2).findEmpresas(Inicio.empresaGlobal.getIdEmpresa()));
                ctlProv.destroy(proveedor.getIdProveedor());
                actualizarDatosTabla();
                Limpiar();
                JOptionPane.showMessageDialog(null, "Se ha eliminado con exito");
            } catch (Exception ex) {
                Logger.getLogger(frmProveedores.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe de ingresar al meno el nombre");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void recoderDatos() {
        if (txtIdProveedor.getText().length() > 0) {
            proveedor = new ProveedoresJpaController(emf).findProveedores(Integer.parseInt(txtIdProveedor.getText()));
        } else {
            proveedor = new Proveedores();
        }
        proveedor.setNombProveedor(txtNomb.getText());
        proveedor.setRegIVA(txtRegIva.getText());
        proveedor.setTel(txtTel.getText());
        proveedor.setDirec(txtDire.getText());
        proveedor.setDoc(txtDoc.getText());
    }

    public void Limpiar() {
        btnEliminar.setEnabled(false);
        btnModi.setEnabled(false);
        btnAgregar.setEnabled(true);
        tblProveedores.clearSelection();
        proveedor = null;
        txtIdProveedor.setText("");
        txtDire.setText("");
        txtDoc.setText("");
        txtNomb.setText("");
        txtRegIva.setText("");
        txtTel.setText("");
    }

    private boolean validarNombre() {
        if (txtNomb.getText().length() > 0) {
            return true;
        } else {
            return false;
        }
    }

