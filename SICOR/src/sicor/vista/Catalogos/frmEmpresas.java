    private boolean estadoBotones = true;
    
    private void estadoBotones(boolean a){
        btnModificar.setEnabled(this.estadoBotones);
        btnEliminar.setEnabled(this.estadoBotones);
        this.estadoBotones = a;
        btnAgregar.setEnabled(this.estadoBotones);
    }

    private List<Empresas> mostrarEmpresas() {
        try {
            EmpresasJpaController ctrEmpresas = new EmpresasJpaController(emf);
            return ctrEmpresas.findEmpresasEntities();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Ocurrio un error: " + e.toString());
            return null;
        }
    }

    private void limpiar() {
        tblDatosEmpresa.clearSelection();
        frmEmpresas_txtCodEmpresa.setText("");
        frmEmpresas_txtRSocial.setText("");
        frmEmpresas_txtRepresentante.setText("");
        frmEmpresas_txtTelefono.setText("");
        if(!estadoBotones)
        estadoBotones(true);
    }

    private boolean validarDatos() {
        return (frmEmpresas_txtCodEmpresa.getText().length() == 0) ? !(frmEmpresas_txtRSocial.getText().length() == 0) : true;
    }
    
     private void ingreDatosEmpresaSinID() {
        empresa = new Empresas();
        empresa.setCodEmpresa(frmEmpresas_txtCodEmpresa.getText());
        empresa.setRazonSocial(frmEmpresas_txtRSocial.getText());
        empresa.setRepre(frmEmpresas_txtRepresentante.getText());
        empresa.setTel(frmEmpresas_txtTelefono.getText());
    }

    private void ingreDatosEmpresaConID() {
        EmpresasJpaController ctrEmpresas = new EmpresasJpaController(emf);
        empresa = ctrEmpresas.findEmpresas(Integer.parseInt(frmEmpresa_idEmpresa.getText()));
        empresa.setCodEmpresa(frmEmpresas_txtCodEmpresa.getText());
        empresa.setRazonSocial(frmEmpresas_txtRSocial.getText());
        empresa.setRepre(frmEmpresas_txtRepresentante.getText());
        empresa.setTel(frmEmpresas_txtTelefono.getText());
    }

