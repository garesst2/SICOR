    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (validarNombre()) {
            ProveedoresJpaController ctlProv = new ProveedoresJpaController(emf);
            recoderDatos();
            proveedor.setIdEmpresa(new EmpresasJpaController(emf2).findEmpresas(Inicio.empresaGlobal.getIdEmpresa()));
            ctlProv.create(proveedor);
            actualizarDatosTabla();
            Limpiar();
            JOptionPane.showMessageDialog(null, "Se ha ingresado con exito");
        } else {
            JOptionPane.showMessageDialog(null, "Debe de ingresar al meno el nombre");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tblProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProveedoresMouseClicked
        if (tblProveedores.getSelectedRowCount() == 0 || tblProveedores.getSelectedRowCount() == 1) {
            btnEliminar.setEnabled(true);
            btnModi.setEnabled(true);
            btnAgregar.setEnabled(false);
        }
    }//GEN-LAST:event_tblProveedoresMouseClicked

    private void btnModiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModiActionPerformed
        if (validarNombre()) {
            try {
                ProveedoresJpaController ctlProv = new ProveedoresJpaController(emf);
                recoderDatos();
                proveedor.setIdEmpresa(new EmpresasJpaController(emf2).findEmpresas(Inicio.empresaGlobal.getIdEmpresa()));
                ctlProv.edit(proveedor);
                actualizarDatosTabla();
                Limpiar();
                JOptionPane.showMessageDialog(null, "Se ha modificado con exito");
            } catch (Exception ex) {
                Logger.getLogger(frmProveedores.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe de ingresar al meno el nombre");
        }
    }//GEN-LAST:event_btnModiActionPerformed

